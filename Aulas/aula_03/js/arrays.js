//Arrays
var listaNomes = ["gatos", "gotas", "gelos", "geleias"]
console.log(listaNomes) // imprimir valores

listaNomes.push("galos")
console.log(listaNomes) // adiciona mais um item na lista 

listaNomes.pop()
console.log(listaNomes) // remove ultimo item da lista 

listaNomes.shift()
console.log(listaNomes) // remove primeiro item da lista 

listaNomes.sort()
console.log(listaNomes) // ordena lista 

listaNomes.reverse()
console.log(listaNomes) // inverte a ordem dos itens da lista 


